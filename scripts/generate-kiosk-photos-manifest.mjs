import { readdirSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";

const PHOTO_EXT = /\.(jpe?g|png|webp|avif)$/i;
const photosDir = join(process.cwd(), "public", "kiosk-photos");
const outFile = join(process.cwd(), "src", "lib", "kiosk-photos-manifest.ts");

let entries = [];
try {
  entries = readdirSync(photosDir);
} catch (err) {
  console.warn(`[kiosk-photos] could not read ${photosDir}: ${err.message}`);
}

const photos = entries
  .filter((name) => PHOTO_EXT.test(name))
  .sort()
  .map((name) => ({
    src: `/kiosk-photos/${name}`,
    alt: name.replace(PHOTO_EXT, "").replace(/[-_]+/g, " ").trim(),
  }));

const out = `// AUTO-GENERATED by scripts/generate-kiosk-photos-manifest.mjs
// Do not edit by hand. Regenerated on each build via the prebuild npm script.

export type KioskPhoto = {
  src: string;
  alt: string;
};

export const KIOSK_PHOTOS_MANIFEST: KioskPhoto[] = ${JSON.stringify(photos, null, 2)};
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, out);
console.log(`[kiosk-photos] wrote ${outFile} (${photos.length} entries)`);
